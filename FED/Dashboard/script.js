// sidebar toggle

var sidebarOpen = false;
var sidebar = document.getElementsByClassName('sidebar');
console.log(sidebar);

function openSidebar() {
  if (!sidebarOpen) {
    sidebar.classList.add('sidebar-responsive');
    sidebarOpen = true;
  }
}

function closeSidebar() {
  if (sidebarOpen) {
    sidebar.classList.remove('sidebar-responsive');
    sidebarOpen = false;
  }
}
